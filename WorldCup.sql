CREATE TABLE human (
    id varchar(7) NOT NULL PRIMARY KEY,
    name varchar(50),
    nationality varchar(50),
    Age int

);
CREATE TABLE player (
   id varchar(7) NOT NULL PRIMARY KEY,
   team_id varchar(4),
   number int,
   goal int,
   FOREIGN KEY (id) REFERENCES human(id),
   FOREIGN KEY (team_id) REFERENCES team(id)
);

CREATE TABLE coach (
    id varchar(5) NOT NULL PRIMARY KEY,
    team_id varchar(4),
	FOREIGN KEY (id) REFERENCES human(id),
	FOREIGN KEY (team_id) REFERENCES team(id)
);

CREATE TABLE refree (
    id varchar(5)  NOT NULL PRIMARY KEY,
    type varchar(15),
	FOREIGN KEY (id) REFERENCES human(id)
);

CREATE TABLE card (
    match_id varchar(9) ,
    player_id varchar(7),
    time Time,
	color varchar(10),
	CONSTRAINT pk_card PRIMARY KEY (match_id,player_id,time),
	FOREIGN KEY (match_id) REFERENCES match(id),
	FOREIGN KEY (player_id) REFERENCES player(id)
);

CREATE TABLE refree_team (
    id varchar(5) NOT NULL PRIMARY KEY,
    head_id varchar(5),
    assistant1_id varchar(5),
    assistant2_id varchar(5),
    fourth_id varchar(5),
    var_id varchar(5),

	FOREIGN KEY (head_id) REFERENCES refree(id),
	FOREIGN KEY (assistant1_id) REFERENCES refree(id),
	FOREIGN KEY (assistant2_id) REFERENCES refree(id),
	FOREIGN KEY (fourth_id) REFERENCES refree(id),
	FOREIGN KEY (var_id) REFERENCES refree(id)
);

CREATE TABLE goal (
    minute int,
    match_id varchar(9),
    player_id varchar(7),
    is_penalty bit,

    CONSTRAINT PK_goal PRIMARY KEY (minute,match_id,player_id),
	FOREIGN KEY (match_id) REFERENCES match(id),
	FOREIGN KEY (player_id) REFERENCES player(id)
);


CREATE TABLE match (
    id varchar(9) NOT NULL PRIMARY KEY,
    team1_id varchar(4),
    team2_id varchar(4),
    team1_goals int,
    team2_goals int,
    stage varchar(50),
    stadium_id varchar(5),
    best_player_id  varchar(7),
    referee_team_id varchar(5),

	FOREIGN KEY (team1_id) REFERENCES team(id),
	FOREIGN KEY (team2_id) REFERENCES team(id),
	FOREIGN KEY (stadium_id) REFERENCES stadium(id),
	FOREIGN KEY (best_player_id) REFERENCES player(id),
	FOREIGN KEY (referee_team_id) REFERENCES refree_team(id)
);

CREATE TABLE stadium(
    id varchar(5) NOT NULL PRIMARY KEY,
    name varchar(50),
    city varchar(50),
    stadium_capacity int,
    create_date date,
    is_covered_stadium bit
);

CREATE TABLE team(
    id varchar(4) NOT NULL PRIMARY KEY,
    nationality varchar(50),
    name varchar(3),
    group_number varchar(7)
);

CREATE TABLE substitute(
    match_id varchar(9),
    player_id_in varchar(7),
    player_id_out varchar(7),
    minute Time,
    type varchar(10),
    is_mandatory bit,

    CONSTRAINT PK_substitute PRIMARY KEY (match_id,player_id_out,player_id_in),
	FOREIGN KEY (match_id) REFERENCES match(id),
	FOREIGN KEY (player_id_out) REFERENCES player(id),
    FOREIGN KEY (player_id_in) REFERENCES player(id)
);



