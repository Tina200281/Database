SELECT Team ,Round(goal*1.0/num_match, 2) as Avg
FROM
    (
    SELECT team1_id as team, SUM(goal1 + goal2) as goal, SUM(team1 + team2) as num_match
    FROM
        (
            SELECT team1_id, SUM(team1_goals) as goal1, COUNT(team1_id) as team1
            FROM match
            GROUP BY team1_id
        )join(
            SELECT team2_id , SUM(team2_goals) as goal2 , COUNT(team2_id) as team2
            FROM match
            GROUP BY team2_id
        ) on team1_id = team2_id
    GROUP BY Team)
ORDER BY Avg
